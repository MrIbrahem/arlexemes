def convert_data(data):
    new_data = {}

    for x in data:
        lemma = x.get("lemma", "")
        # ---
        new_data.setdefault(lemma, {"items": {}, "proplabels": {}})
        # ---
        item1 = x.get("1_item", "")
        item2 = x.get("2_item", "")
        # ---
        new_data[lemma]["items"].setdefault(item1, {"category": {}, "props": {}})
        new_data[lemma]["items"].setdefault(item2, {"category": {}, "props": {}})
        # ---
        item1_category = x.get("1_categoryLabel", "")
        item2_category = x.get("2_categoryLabel", "")
        # ---
        if item1_category:
            new_data[lemma]["items"][item1]["category"] = item1_category
        # ---
        if item2_category:
            new_data[lemma]["items"][item2]["category"] = item2_category
        # ---
        p = x.get("p", "")
        pLabel = x.get("pLabel", "")
        # ---
        if not p:
            continue
        # ---
        if pLabel:
            new_data[lemma]["proplabels"].setdefault(p, pLabel)
        # ---
        new_data[lemma]["items"][item1]["props"].setdefault(p, {"value": "", "valuelabel": ""})
        new_data[lemma]["items"][item2]["props"].setdefault(p, {"value": "", "valuelabel": ""})
        # ---
        prop_value1 = x.get("1_p_value", "")
        prop_value1Label = x.get("1p_valueLabel", "")
        # ---
        if prop_value1:
            new_data[lemma]["items"][item1]["props"][p]["value"] = prop_value1
        # ---
        if prop_value1Label:
            new_data[lemma]["items"][item1]["props"][p]["valuelabel"] = prop_value1Label
        # ---
        prop_value2 = x.get("2_p_value", "")
        prop_value2Label = x.get("2p_valueLabel", "")
        if prop_value2:
            new_data[lemma]["items"][item2]["props"][p]["value"] = prop_value2
        # ---
        if prop_value2Label:
            new_data[lemma]["items"][item2]["props"][p]["valuelabel"] = prop_value2Label
    # ---
    return new_data


